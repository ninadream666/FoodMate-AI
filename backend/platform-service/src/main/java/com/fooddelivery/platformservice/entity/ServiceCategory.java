package com.fooddelivery.platformservice.entity;

/**
 * 服务类别枚举
 */
public enum ServiceCategory {
    /**
     * 基础服务（如技术服务费）
     */
    BASIC,

    /**
     * 流量服务（如首页推荐、搜索置顶）
     */
    TRAFFIC,

    /**
     * 配送服务（如平台配送、优先配送）
     */
    DELIVERY,

    /**
     * 运营服务（如数据报表、客服代运营）
     */
    OPERATION
}
